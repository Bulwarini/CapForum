        <p class="has-text-white is-size-4 has-text-weight-bold is-align-self-flex-start">Kosmiczny Teleskop Jamesa Webba</p><br>
          <p class="has-text-white is-size-4 ">Teleskop ten powstawał w latach 2007-2021,
             na zlecenie NASA. Miał on być dopełnieniem teleskopu Hubble’a. Został on nazwany na cześć drugiego administratora NASA, Jamesa Webba.</p><br>
             <img src="\obrazki\jwst\jwst.jpg" height="500rem" width="500rem"><br>
          <p class="has-text-white is-size-4 has-text-weight-bold is-align-self-flex-start">Europa</p>
          <p class="has-text-white is-size-4 ">Księżyc Jowisza – Europa, jest jednym z niewielu światów w naszym Układzie Słonecznym, który mógłby potencjalnie stwarzać warunki
             dla życia. Poprzednie badania pokazały, że poniżej swojej lodowej powierzchni, skrywa się ocean ciekłej wody z kamienistym dnem. Jednakże naukowcy nie potwierdzili
              czy ocean zawierał związki chemiczne potrzebne do życia, szczególnie węgiel. <br><br>Astronomowie posługując się danymi z JWST zidentyfikowali dwutlenek węgla w jednym
               z regionów na lodowej powierzchni Europy. Analiza wskazuje, że ten węgiel najprawdopodobniej wydobył się z podpowierzchni oceanu i nie został tam dostarczony przez
                żadne meteory ani inne źródła zewnętrzne. Nota bene znajduje się tam od całkiem niedawna (patrząc na to ze skala geologicznej). To odkrycie wprowadza ważne implikacje
                 dla potencjalnej zamieszkiwalności europejskiego oceanu.</p><br>
                 <img src="\obrazki\jwst\Europa.png" height="800rem" width="800rem"><br>
          <p class="has-text-white is-size-4 has-text-weight-bold is-align-self-flex-start">Połączenie powierzchnia-ocean</p><br>
          <p class="has-text-white is-size-4">Webb udowadnia, że na powierzchni Europy, dwutlenek węgla jest najbardziej obfity na obszarze nazwanym
             Tara Regio – geologicznie młody region ogólnie przekształconego terenu znanego jako „obszar chaosu”. Powierzchnia lodu została naruszona
              i prawdopodobnie dochodziło tam do wymian materiału pomiędzy podpowierzchniowym oceanem, a lodową powierzchnią. <br><br>Naukowcy zidentyfikowali dwutlenek węgla
               używając danych ze specjalnego modułu teleskopu, służącego do rozkładania światła na czynniki pierwsze (zdjęcia w podczerwieni).
                To narzędzie zapewnia zakres widma z rozdzielczością 320x320 kilometrów na powierzchni Europy, której średnica wynosi 3128 kilometrów,
                 pozwalając astronomom na zlokalizowanie odpowiednich związków chemicznych.</p> <br>
