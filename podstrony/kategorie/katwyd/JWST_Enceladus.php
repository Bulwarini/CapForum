          <p class="has-text-white is-size-4 has-text-justified">Gejzer pary wodnej z powierzchni Enceladusa, rozciągający się na ponad 9,5 tys. km (dystans z Los Angeles do stolicy Argentyny) – został
             wykryty przez naukowców wykorzystujących Kosmiczny Teleskop Jamesa Webba. Nie tylko jest to pierwszy raz, gdy zaobserwowano emisję tak dużej ilości wody
              na tak rozgległym obszarze, ale Webb daje także badaczom bezpośredni wgląd (po raz pierwszy) na to jak taka emisja wpływa na zasób wód całego układu Saturna i jego
