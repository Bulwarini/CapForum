                w Układzie Słonecznym, związanych z poszukiwaniu pozaziemskiego życia. Umiejscowiony pomiędzy zewnętrzną skorupą księżyca i jego skalistym jądrem znajduje
                 się wielki słony ocean. Przypominające gejzery wulkany wyrzucają strumienie cząsteczek lodu, pary wodnej i organicznych związków chemicznych ze szczelin
